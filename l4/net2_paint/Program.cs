void PrintArray(int[,] matr)
{
    int rows = matr.GetLength(0);
    int cols = matr.GetLength(1);

    for (int i = 0; i < rows; i++)
    {
        for (int j = 0; j < cols; j++)
        {
            if (matr[i, j] == 0) Console.Write("  ");
            else Console.Write($"{matr[i, j]} ");
        }
        Console.WriteLine();
    }
}

